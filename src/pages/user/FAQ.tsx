const FAQ = () => {
  useTitle('FAQ');
  const { config } = useEventConfig();
  const [searchQuery, setSearchQuery] = useState<string>('');

  const formattedDateText = useMemo(() => {
    if (config.date.length === 0) {
      return '';
    }

    const toParts = (dateText: string) => {
      const [year, month, day] = dateText
        .split('-')
        .map((value) => Number(value));
      return { year, month, day };
    };

    const first = toParts(config.date[0]);
    const last = toParts(config.date[config.date.length - 1]);

    if (first.year === last.year && first.month === last.month) {
      return `${first.month}/${first.day}~${last.day}`;
    }

    return `${first.month}/${first.day}~${last.year}/${last.month}/${last.day}`;
  }, [config.date]);

  const getAnswer = (item: FAQItem) => {
    if (item.question === '開催日はいつですか?') {
      return `${config?.year}年度の外苑祭は ${formattedDateText} に開催されます。`;
    }
    if (item.question === '開催場所はどこですか?') {
      return (
        <>
          都立青山高校で行われます。アクセスは
          <a
            href='https://www.metro.ed.jp/aoyama-h/access/access.html'
            target='_blank'
          >
            アクセス
          </a>
          をご覧ください。
        </>
      );
    }
    if (item.question === '各クラスの公演で、何を上演するのかや、時間を知りたいです。') {
      return (
        <>
          <a href='/performances'>公演一覧</a>や
          <a href='/timetable'>タイムテーブル</a>をご覧ください。
        </>
      );
    }
    if (item.question === '取得したチケット一覧はどこで見れますか?' && item.category === '電子チケットの使い方(青高生用)') {
      return (
        <>
          <a href='/students/dashboard'>ダッシュボード</a>あるいは
          <a href='/t'>チケット履歴</a>からご確認いただけます。画面右上の ≡
          (3本線のメニュー)をタップし、「生徒用ページ」や「チケット」から移動することも可能です。
        </>
      );
    }
    if (item.question === '取得したチケット一覧はどこで見れますか?' && item.category === '中学生の方へ') {
      return (
        <>
          <a href='/junior/mypage'>マイページ</a>あるいは
          <a href='/t'>チケット履歴</a>からご確認いただけます。画面右上の ≡
          (3本線のメニュー)をタップし、「中学生用ページ」や「チケット」から移動することも可能です。
        </>
      );
    }
    if (item.question === 'どこで申し込みができますか?') {
      return (
        <>
          <a
            href='https://www.metro.ed.jp/aoyama-h/news/2026/07/8_2.html'
            target='_blank'
            rel='noopener noreferrer'
          >
            都立青山高校ホームページ
          </a>
          にて、7月31日まで受付をしております。
        </>
      );
    }
    if (item.question === 'その他エラーが発生します。') {
      return (
        <>
          エラーメッセージとともに、外苑祭総務にお問い合わせいただくか、
          <a
            href='https://docs.google.com/forms/d/e/1FAIpQLSfGsEXv2e1IoDbF2RjhrCyK5myHU0Dq-YJ4_3dHMhNeLAvjUg/viewform?usp=dialog'
            target='_blank'
            rel='noopener noreferrer'
          >
            お問い合わせ
          </a>
          よりご連絡をお願いします。
        </>
      );
    }
    if (item.question === 'このサイトのソースコードを使用することはできますか?') {
      return (
        <>
          このサイトのソースコードはMITライセンスで公開されており、誰でも自由に使用、改変、配布することができます。使用の際に事前連絡は不要です。詳細は
          <a
            href='https://github.com/gaiensaisoumu/gaiensai'
            target='_blank'
            rel='noopener noreferrer'
          >
            GitHubリポジトリ
          </a>
          をご覧ください。
        </>
      );
    }
    if (item.question === 'バグと思われる部分を見つけました。') {
      return (
        <>
          ご連絡ありがとうございます。バグ報告は
          <a
            href='https://docs.google.com/forms/d/e/1FAIpQLSfGsEXv2e1IoDbF2RjhrCyK5myHU0Dq-YJ4_3dHMhNeLAvjUg/viewform?usp=dialog'
            target='_blank'
            rel='noopener noreferrer'
          >
            お問い合わせ
          </a>
          からお願いします。
        </>
      );
    }
    return item.answer;
  };

  const filteredFAQData = useMemo(() => {
    if (!searchQuery) {
      return FAQ_DATA;
    }

    const keywords = searchQuery.toLowerCase().split(/\s+/).filter((k) => k.length > 0);

    return FAQ_DATA.filter((item) => {
      const category = item.category.toLowerCase();
      const question = item.question.toLowerCase();
      const answer = item.answer.toLowerCase();
      const searchText = item.searchText?.toLowerCase() || '';

      // 動的に生成される回答のテキストを取得
      const dynamicAnswer = getAnswer(item);
      const dynamicAnswerText = typeof dynamicAnswer === 'string' ? dynamicAnswer.toLowerCase() : '';

      return keywords.every(
        (keyword) =>
          category.includes(keyword) ||
          question.includes(keyword) ||
          answer.includes(keyword) ||
          searchText.includes(keyword) ||
          dynamicAnswerText.includes(keyword)
      );
    });
  }, [searchQuery, config, formattedDateText]);

  const groupedFAQData = useMemo(() => {
    const groups: Record<string, FAQItem[]> = {};
    filteredFAQData.forEach((item) => {
      if (!groups[item.category]) {
        groups[item.category] = [];
      }
      groups[item.category].push(item);
    });
    return groups;
  }, [filteredFAQData]);

  return (
    <>
      <h1 className={styles.pageTitle}>FAQ</h1>
      <div className={styles.searchInputWrapper}>
        <input
          type='text'
          placeholder='キーワードで検索...'
          value={searchQuery}
          onInput={(e) => setSearchQuery((e.target as HTMLInputElement).value)}
          className={styles.searchInput}
        />
      </div>
      {Object.entries(groupedFAQData).map(([category, items]) => (
        <NormalSection key={category}>
          <h2>{category}</h2>
          {items.map((item, index) => (
            <div key={`${category}-${index}`}>
              <h3>Q. {item.question}</h3>
              <p>{getAnswer(item)}</p>
            </div>
          ))}
        </NormalSection>
      ))}
      {filteredFAQData.length === 0 && (
        <p style={{ textAlign: 'center', padding: '20px' }}>
          検索結果が見つかりませんでした。
        </p>
      )}
    </>
  );
};

export default FAQ;
